    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
CustomerDAO dao;
dao = new CustomerDAO();
int loja = 0;
if (jComboBox1.getSelectedItem() == "Americanas" ) {
loja = 1;
}else if (jComboBox1.getSelectedItem() == "Magazine Luiza" ){
loja = 2;
}
int endereco = 0;
if (jComboBox2.getSelectedItem() == "Macau") {
endereco = 200;
}else if (jComboBox2.getSelectedItem() == "Pendencias"  ){
endereco = 400;
}
int active = 0;
if (jRadioButton1.isSelected()) {
active = 1;
}else if (jRadioButton2.isSelected()){
active = 0;
}
dao.updateCustomer(Integer.parseInt(jTextField4.getText()) , loja, jTextField1.getText(), jTextField2.getText(), jTextField3.getText(), endereco, active);
JOptionPane.showMessageDialog(null, "Seu Cliente  foi Atualizado");
setVisible(false);
    }                                        

